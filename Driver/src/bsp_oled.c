/*
 *  Send one char one the panel 
 */
void OLED_W_ASCII(uint8_t a)
{
  uint8_t j,temp;
  
  temp = a-32;
  
  for(j=0;j<6;j++)
  {
		SSD1306_WrByte(ASCII[temp][j]);
  }

}


